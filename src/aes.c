static const uint8_t rcon[11] = {
  0x8d, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36,
};

static void xor_block(uint8_t *a, const uint8_t *b) {
    for(uint8_t i = 0; i < AES_BLOCKLEN; i++)
        a[i] ^= b[i];
}

static void add_round_key(state_t *state, const uint8_t *round_key, uint8_t round) {
    /* printf("add_round_key %d\n", round); */
    uint8_t i, j;
    for(i = 0; i < 4; i++)
        for(j = 0; j < 4; j++)
            (*state)[i][j] ^= round_key[round*16 + i*4 + j];
}

static void sub_bytes(state_t *state) {
    /* printf("sub_bytes\n"); */
    uint8_t i, j;
    for(i = 0; i < 4; i++)
        for(j = 0; j < 4; j++)
            (*state)[i][j] = sbox[(*state)[i][j]];
}

static void shift_rows(state_t *state) {
    /* printf("shift_rows\n"); */
    uint8_t temp;

    temp = (*state)[0][1];
    (*state)[0][1] = (*state)[1][1];
    (*state)[1][1] = (*state)[2][1];
    (*state)[2][1] = (*state)[3][1];
    (*state)[3][1] = temp;

    temp = (*state)[0][2];
    (*state)[0][2] = (*state)[2][2];
    (*state)[2][2] = temp;
    temp = (*state)[1][2];
    (*state)[1][2] = (*state)[3][2];
    (*state)[3][2] = temp;

    temp = (*state)[0][3];
    (*state)[0][3] = (*state)[3][3];
    (*state)[3][3] = (*state)[2][3];
    (*state)[2][3] = (*state)[1][3];
    (*state)[1][3] = temp;
}

static uint8_t xtime(uint8_t x) {
    return ((x<<1) ^ (((x>>7) & 1) * 0x1b));
}

static void mix_columns(state_t *state) {
    /* printf("mix_columns\n"); */
    uint8_t i, tmp, tm, t;
    for (i = 0; i < 4; ++i)
    {
        t   = (*state)[i][0];
        tmp = (*state)[i][0] ^ (*state)[i][1] ^ (*state)[i][2] ^ (*state)[i][3];
        tm  = (*state)[i][0] ^ (*state)[i][1];  tm = xtime(tm);  (*state)[i][0] ^= tm ^ tmp;
        tm  = (*state)[i][1] ^ (*state)[i][2];  tm = xtime(tm);  (*state)[i][1] ^= tm ^ tmp;
        tm  = (*state)[i][2] ^ (*state)[i][3];  tm = xtime(tm);  (*state)[i][2] ^= tm ^ tmp;
        tm  = (*state)[i][3] ^ t;               tm = xtime(tm);  (*state)[i][3] ^= tm ^ tmp;
    }
}
static void key_expansion(uint8_t *round_key, const uint8_t *key) {
    unsigned i, j, k;
    uint8_t tempa[4];

    for(i = 0; i < AES_KEY_WORD; i++) {
        round_key[i*4 + 0] = key[i*4 + 0];
        round_key[i*4 + 1] = key[i*4 + 1];
        round_key[i*4 + 2] = key[i*4 + 2];
        round_key[i*4 + 3] = key[i*4 + 3];
    }

    for(i = AES_KEY_WORD; i < AES_COLUMNS*(AES_ROUNDS + 1); i++) {
        k = (i - 1)*4;
        tempa[0] = round_key[k + 0];
        tempa[1] = round_key[k + 1];
        tempa[2] = round_key[k + 2];
        tempa[3] = round_key[k + 3];

        if(i % AES_KEY_WORD == 0) {
            /* rot word */
            {
                const uint8_t tmp = tempa[0];
                tempa[0] = tempa[1];
                tempa[1] = tempa[2];
                tempa[2] = tempa[3];
                tempa[3] = tmp;
            }

            /* sbox word */
            tempa[0] = sbox[tempa[0]];
            tempa[1] = sbox[tempa[1]];
            tempa[2] = sbox[tempa[2]];
            tempa[3] = sbox[tempa[3]];

            tempa[0] ^= rcon[i/AES_KEY_WORD];
        }

        j = i*4;
        k = (i - AES_KEY_WORD)*4;
        round_key[j + 0] = round_key[k + 0] ^ tempa[0];
        round_key[j + 1] = round_key[k + 1] ^ tempa[1];
        round_key[j + 2] = round_key[k + 2] ^ tempa[2];
        round_key[j + 3] = round_key[k + 3] ^ tempa[3];
    }
}

/* main AES cipher function */
static void cipher(state_t *state, uint8_t *round_key) {
    uint8_t round = 0;

    add_round_key(state, round_key, round);
    /* printf("cipher 1: ");print_hex((uint8_t *)state, 16); */

    for(round = 1; round < AES_ROUNDS+1; round++) {
        sub_bytes(state);
        /* printf("cipher 2: ");print_hex((uint8_t *)state, 16); */
        shift_rows(state);
        /* printf("cipher 3: ");print_hex((uint8_t *)state, 16); */
        if(round != AES_ROUNDS) mix_columns(state);
        /* printf("cipher 4: ");print_hex((uint8_t *)state, 16); */
        add_round_key(state, round_key, round);
        /* printf("cipher 5: ");print_hex((uint8_t *)state, 16); */
    }
}

static void keygen(uint8_t *key, size_t sz) {
    for(uint8_t i = 0; i < sz; i++)
        key[i] = rand() % 0x100;
}

void ctx_init(struct ctx *ctx, const uint8_t *key, const uint8_t *iv) {
    key_expansion(ctx->round_key, key);
    memcpy(ctx->iv, iv, sizeof ctx->iv);
}

void cbc_encrypt_buf(struct ctx *ctx, uint8_t *buf, size_t sz) {
    size_t i;
    uint8_t *iv = ctx->iv;

    /* printf("encrypt begin: ");print_hex(buf, 16); */
    for(i = 0; i < sz; i += AES_BLOCKLEN) {
        xor_block(buf, iv);
        /* printf("xor_block: ");print_hex(buf, 16); */
        cipher((state_t *)buf, ctx->round_key);
        /* printf("cipher: ");print_hex(buf, 16); */
        iv = buf; /* insecure or so I've heard */
        buf += AES_BLOCKLEN;
    }

    memcpy(ctx->iv, iv, AES_BLOCKLEN);
}

void pad_pkcs7(uint8_t *buf, size_t blocksz, size_t sz) {
    size_t padsz = blocksz - (sz + 1) % blocksz + 1;
    memset(buf + sz, padsz, padsz);
}

static void print_hex(uint8_t *buf, size_t sz) {
    size_t i;
    for(i = 0; i < sz; i++)
        printf("%02X", buf[i]);
    printf("\n");
}

void algo_aes(void) {
    char buf[256] = { 0 };
    size_t len;
    struct ctx ctx;
    uint8_t key[AES_KEYLEN], iv[AES_BLOCKLEN];

    keygen(key, sizeof key);
    keygen(iv, sizeof key);
    ctx_init(&ctx, key, iv);

    printf("plaintext: ");
    fgets(buf, sizeof buf, stdin);
    buf[len = strcspn(buf, "\n")] = '\0';
    pad_pkcs7((uint8_t *)buf, AES_BLOCKLEN, len);
    len = (len+1 + 0xf) & ~0xf;

    cbc_encrypt_buf(&ctx, (uint8_t *)buf, len);
    printf("ciphertext: "); print_hex((uint8_t *)buf, len);
    printf("key: "); print_hex((uint8_t *)key, sizeof key);
    printf("iv: "); print_hex((uint8_t *)iv, sizeof iv);
}
